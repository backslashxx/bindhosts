	# read out if Adaway did something 
	grep -q "generated by AdAway" $target_hostsfile && string="description=status: active âœ… | ğŸ›‘ AdAway ğŸ•Šï¸"
	# write it
	sed -i "s/^description=.*/$string/g" $MODDIR/module.prop
